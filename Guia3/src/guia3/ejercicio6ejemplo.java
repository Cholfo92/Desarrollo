    private void btnBorrarEspeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarEspeActionPerformed
        // TODO add your handling code here:
        if(deporte.getItemCount()>0)
        {
            String msj = "";
            msj = deporte.getSelectedItem().toString();
        
            lblMostrar.setText(msj);
        
            confirmarEspe.setVisible(true);
        }
        
        

        
    }//GEN-LAST:event_btnBorrarEspeActionPerformed

    private void confirmarEspeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarEspeActionPerformed
        // TODO add your handling code here:
        if(deporte.getItemCount()>0) deporte.removeItemAt(deporte.getSelectedIndex());
        
    }//GEN-LAST:event_confirmarEspeActionPerformed

