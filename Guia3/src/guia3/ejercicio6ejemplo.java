    private void confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarActionPerformed
        // TODO add your handling code here:
        String msj = "";
        
        if(!txtAgregar.getText().isEmpty())
        {  
            msj = txtAgregar.getText();
            txtAgregar.setText("");
            deporte.addItem(msj);
        }
        
        agregar(false);
    }//GEN-LAST:event_confirmarActionPerformed

    private void borrarTodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarTodoActionPerformed
        // TODO add your handling code here:
        if(deporte.getItemCount()>0) deporte.removeAllItems();
        else{
            JOptionPane.showMessageDialog(rootPane, "Lista Vacia");
        }
        
        
    }//GEN-LAST:event_borrarTodoActionPerformed

